package net.revtut.libraries.games.player;

/**
 * Player Statistics
 */
public enum PlayerStatistic {
    /**
     * Number of games won
     */
    GAMES_WON,

    /**
     * Number of games lost
     */
    GAMES_LOST,

    /**
     * Number of games played
     */
    GAMES_PLAYED,

    /**
     * Play time on the game
     */
    GAME_PLAY_TIME,

    /**
     * Number of kills
     */
    GAME_KILLS,

    /**
     * Number of deaths
     */
    GAME_DEATHS,

    /**
     * Number of flags captured
     */
    GAME_FLAGS_CAPTURED,

    /**
     * Number of attempts to capture a flag
     */
    GAME_FLAGS_CAPTURE_ATTEMPTS
}