package net.revtut.libraries.games.arena;

/**
 * Arena flags
 */
public enum ArenaFlag {
    /**
     * Break a block
     */
    BLOCK_BREAK,

    /**
     * Place a block
     */
    BLOCK_PLACE,

    /**
     * Empty a bucket
     */
    BUCKET_EMPTY,

    /**
     * Fill a bucket
     */
    BUCKET_FILL,

    /**
     * Chat
     */
    CHAT,

    /**
     * Drop a item to the ground
     */
    DROP_ITEM,

    /**
     * Damage a entity
     */
    DAMAGE,

    /**
     * Entity hunger
     */
    HUNGER,

    /**
     * Click on item on inventory
     */
    INVENTORY_CLICK,

    /**
     * Interact
     */
    INTERACT,

    /**
     * Move
     */
    MOVE,

    /**
     * Pickup a dropped item
     */
    PICKUP_ITEM,

    /**
     * Weather change
     */
    WEATHER;
}