package net.revtut.libraries.minecraft.bukkit.games.arena;

/**
 * Enum with the arena preference for using when getting a arena
 */
public enum ArenaPreference {

    /**
     * The preference is the arena with more players
     */
    MORE_PLAYERS,

    /**
     * The preference is the arena with less players
     */
    LESS_PLAYERS,

    /**
     * The preference is the arena with more remaining time
     */
    MORE_REAMINING_TIME,

    /**
     * The preference is the arena with less remaining time
     */
    LESS_REMAINING_TIME,
}
